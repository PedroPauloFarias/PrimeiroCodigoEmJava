public class MultiplicandoInicio {
    public static void main(String[] args){

        int x; //Valores inteiros
        double y; // Valores quebrados

        x = 5; // Aqui o "x" recebe o número 5
        y = 2 * x; // Aqui o "y", recebe o número de "x" vezes o 2
        System.out.println(x); // Aqui mostra o valor de x
        System.out.println(y); // Aqui mostra o valor de y

    }
}
